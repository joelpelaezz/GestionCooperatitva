    JDAsamblea(java.awt.Frame parent, boolean modal, int idAsamblea,int op) {
        // este constructor se utiliza para ver una asamblea ya creada
        
       
         super(parent, modal);
        initComponents();
        this.setTitle("Asamblea");
        //configuraacion de la ventana
        //Orden del dia
        panelTema.setVisible(false);
        panelSolucionTema.setVisible(false);
  
         
        this.botonSeleecionado= op;
        this.idAsambleaEdit= idAsamblea;
        
        // cargar en la ventana con los valores guardados
        AsambleaDao asambleas = new AsambleaDaoImp();
        Asamblea asamblea = asambleas.getAsamblea(idAsamblea);
        
        
        
        //paso los datos a la  ventana configuracion asamblea
        int indexTipoAsmablea = getIndexCmbTipoAsamblea(asamblea.getTipoAsamblea().toString());
        String lugar = asamblea.getLugar();
        Date fecha = asamblea.getFecha();
                
         txtLugar.setText(lugar);
         cmbElegirAsamblea.setSelectedIndex(indexTipoAsmablea);
         fechaAsamblea.setDate(fecha);
        //..................... 
            
        // paso los datos de la tabla ASISTENCIA_ASAMBLEA a la Jtable asistencia
                     
            TablaUtil.prepararTablaAsambleaAsociado(modelo, tblAsistencia);
            modelo = (DefaultTableModel)tblAsistencia.getModel();
            for (AsistenciaAsamblea asistAsamblea : asamblea.getAsistenciaAsambleas()) {
                Asociado a = asistAsamblea.getAsociados();
                // estos datos deben pasarse a la tabla
                int idAsociado = a.getIdAsociado();
                String nombreAsociado = a.getNombreAsociado()+"  "+a.getApellidoAsociado();
                String horaEntrada = FechaUtil.getHora(asistAsamblea.getHoraLlegada());
                String horaSalida = FechaUtil.getHora(asistAsamblea.getHoraSalida());
               
               Object[] filaAsociado = {idAsociado,nombreAsociado,horaEntrada,horaSalida};
               modelo.addRow(filaAsociado);
               
               }
            
            // paso los datos del orden del dia
            Set<OrdenDia> listaOrdenDia = asamblea.getOrdenDias();
           TablaUtil.prepararTablaOrdendelDia(modelo, tablaOrdenDia);
           TablaUtil.cargarModeloOrdenDelDia(modelo,listaOrdenDia , tablaOrdenDia); 
            
            
    }

