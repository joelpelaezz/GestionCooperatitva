private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

    //CAPTURAR LOS DATOS DE CONFIGUARACION DE LA ASAMBLEA
    if (botonSeleecionado==1) {// se crea una asamblea
        String tipoAsablea =  cmbElegirAsamblea.getSelectedItem().toString();
        Date fechaAsamb =  fechaAsamblea.getDate();
        
        String horaIni= cmbHoraIninicio.getSelectedItem().toString()+":"+cmbMinInicio.getSelectedItem().toString();
        Date horaInicio = Util.FechaUtil.getHora(horaIni);
        
        String horaFi= cmbHoraFin.getSelectedItem().toString()+":"+cmbMinFin.getSelectedItem().toString();
        Date horaFin = Util.FechaUtil.getHora(horaFi);
        
        String lugar = txtLugar.getText();
        String obsercacion = txtObservacion.getText();
        
        // guardo la asamblea en la bd
        AsambleaDao Asambleas = new AsambleaDaoImp();
        Asamblea a = new Asamblea(tipoAsablea, fechaAsamb, horaInicio, horaFin, 0, lugar, null, null);
        Asambleas.addAsamblea(a);
    
    
    }else{
       if(botonSeleecionado==2){  //editar
        // capturar los y datos de la tabla asistencia 
           modelo= (DefaultTableModel) tblAsistencia.getModel();
           if (modelo.getRowCount()!=0) { // si contiene datos la tabla entonces guardarlo
               AsambleaDao asambleas = new AsambleaDaoImp();
               AsociadoDao asociados = new AsociadoDaoImp();
               AsistenciaAsambleaDao asistenciasAsamblea = new AsistenciaAsambleaDaoImp();
               Asamblea asamblea = asambleas.getAsamblea(idAsambleaEdit);
               
               Asociado asocidado;
               for (int fila = 1; fila < modelo.getRowCount(); fila++) {
                   int idAsociado = Integer.parseInt(modelo.getValueAt(fila, 0).toString());
                   asocidado = asociados.getAsociado(idAsociado);
                   Date horaEntrada = FechaUtil.getHora(modelo.getValueAt(fila, 2).toString());
                   Date horaSalida = FechaUtil.getHora(modelo.getValueAt(fila, 3).toString());
                   AsistenciaAsamblea asistenciaAsamblea = new AsistenciaAsamblea(asocidado, asamblea, horaEntrada, horaSalida);
                   asistenciasAsamblea.addAsistenciaAsamblea(asistenciaAsamblea);

               }
           }
          
          //capturar los datos del orden del dia
        
        
        
        
       } 
    }
    
    
}//GEN-LAST:event_btnGuardarActionPerformed

